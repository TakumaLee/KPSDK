package org.android.kpsdk.entity;

public class Music {
	
	private int musicID;
	private String soundcloudID;
	private String votes;
	private String song_name;
	private String groupname;
	private String link;
	private String creative;
	private String tags;
	private long track_id;
	private String waveform_url;
	private String stream_url;
	private int play_count;
	private int favoritings_count;
	private String soundcloud_url;
	public int getMusicID() {
		return musicID;
	}
	public void setMusicID(int musicID) {
		this.musicID = musicID;
	}
	public String getSoundcloudID() {
		return soundcloudID;
	}
	public void setSoundcloudID(String soundcloudID) {
		this.soundcloudID = soundcloudID;
	}
	public String getVotes() {
		return votes;
	}
	public void setVotes(String votes) {
		this.votes = votes;
	}
	public String getSong_name() {
		return song_name;
	}
	public void setSong_name(String song_name) {
		this.song_name = song_name;
	}
	public String getGroupname() {
		return groupname;
	}
	public void setGroupname(String groupname) {
		this.groupname = groupname;
	}
	public String getLink() {
		return link;
	}
	public void setLink(String link) {
		this.link = link;
	}
	public String getCreative() {
		return creative;
	}
	public void setCreative(String creative) {
		this.creative = creative;
	}
	public String getTags() {
		return tags;
	}
	public void setTags(String tags) {
		this.tags = tags;
	}
	public long getTrack_id() {
		return track_id;
	}
	public void setTrack_id(long track_id) {
		this.track_id = track_id;
	}
	public String getWaveform_url() {
		return waveform_url;
	}
	public void setWaveform_url(String waveform_url) {
		this.waveform_url = waveform_url;
	}
	public String getStream_url() {
		return stream_url;
	}
	public void setStream_url(String stream_url) {
		this.stream_url = stream_url;
	}
	public int getPlay_count() {
		return play_count;
	}
	public void setPlay_count(int play_count) {
		this.play_count = play_count;
	}
	public int getFavoritings_count() {
		return favoritings_count;
	}
	public void setFavoritings_count(int favoritings_count) {
		this.favoritings_count = favoritings_count;
	}
	public String getSoundcloud_url() {
		return soundcloud_url;
	}
	public void setSoundcloud_url(String soundcloud_url) {
		this.soundcloud_url = soundcloud_url;
	}

}
